#Printing the square roots of the perfect square numbers

x = 4
y = 9
z = 16

print("The square root of", x, "is", x**0.5)
print("The square root of", y, "is", y**0.5)
print("The square root of", z, "is", z**0.5)

#Printing the square root of imperfect square numbers

p = 5
q = 11
r = 21

print("The square root of", p, "is", p**0.5)
print("The square root of", q, "is", q**0.5)
print("The square root of", r, "is", r**0.5)