#Checking whether the number is even or odd
number1 = int(input("Enter a number: "))
if number1 % 2 == 0:
    print("The number is even.")
else:
    print("The number is odd.")

number2 = int(input("Enter another number:"))
if number2 % 2 == 0:
    print("The number is even.")
else:
    print("The number is odd.")